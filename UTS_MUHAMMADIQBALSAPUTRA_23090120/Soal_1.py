# deklarasi
bil1 = int(input('Masukan Bilangan Pertama : '))
bil2 = int(input('Masukan Bilangan Kedua : '))

# operasi
penjumlahan = bil1 + bil2
pengurangan = bil1 - bil2
modulus = bil1%bil2

# output
print(f'Hasil Penjumlahan dari {bil1} dan {bil2} adalah = {penjumlahan}')
print(f'Hasil Pengurangan dari {bil1} dan {bil2} adalah = {pengurangan}')
print(f'Hasil Modulus dari {bil1} dan {bil2} adalah = {modulus}')